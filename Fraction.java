
public class Fraction
{
    private int numerator;
    private int denominator;

    public Fraction(int num, int den)
    {
        numerator = num;
        denominator = den;
    }
    
    public int getNum()
    {
        return numerator;
    }
    
    public int getDem()
    {
       return denominator;
    }
    
}
